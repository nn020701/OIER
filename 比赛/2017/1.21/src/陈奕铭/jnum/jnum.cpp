int meiju(int a,int b)//枚举233
{
	int f[15];
	int ans=0;
	bool flag;
	if(a==0)
		a++;
	for(int i=a;i<=b;i++)
	{
		int t=i,len=0;
		do
			f[len++]=t%10;
		while(t/=10);
		flag=true;
		for(int i=0;i<len-1;i++)
			if(abs(f[i]-f[i+1])<2)
			{
				flag=false;
				break;
			}
		if(flag)
			ans++;
	}
	return ans;
}
